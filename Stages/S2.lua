CurrWeave = 1
NumK = 0
CS = 1
token = true
function Set2.RegEventsLoader()
	MAHelper.ListAddPosition(1,1)
	EVENT_MANAGER:RegisterForEvent(Set2.name .. "PlayerDeath", EVENT_UNIT_DEATH_STATE_CHANGED, Set2.OnPlayerDeath)
	EVENT_MANAGER:RegisterForEvent(Set2.name .."Announcement", EVENT_DISPLAY_ANNOUNCEMENT, Set2.OnTitleAnnounced)
	EVENT_MANAGER:RegisterForEvent(Set2.name .."kill", EVENT_COMBAT_EVENT, Set2.OnUnitKilled)
	EVENT_MANAGER:AddFilterForEvent(Set2.name .."kill", EVENT_COMBAT_EVENT, REGISTER_FILTER_COMBAT_RESULT, ACTION_RESULT_DIED_XP)
end
function Set2.RegUnloadEvents()
	EVENT_MANAGER:UnregisterForEvent(Set2.name .. "PlayerDeath", EVENT_COMBAT_EVENT, Set2.OnPlayerDeath)
	EVENT_MANAGER:UnregisterForEvent(Set2.name .."Announcement", EVENT_DISPLAY_ANNOUNCEMENT, Set2.OnTitleAnnounced)
	EVENT_MANAGER:UnregisterForEvent(Set2.name .."kill", EVENT_COMBAT_EVENT, Set2.OnUnitKilled)
end
function  Set2.OnTitleAnnounced(eventcode, title)
	NumK = 0 
	CurrWeave = 1
	CS = MAHelper.OnTitleReturnRound(title)
	if CS >= 1 then
		MAHelper.RemoveIcons()
		MAHelper.ListAddPosition(CS , 1)
	end
end

function  Set2.OnPlayerDeath( eventCode,  unitTag,  isDead)
	if isDead and unitTag == "player" then
		MAHelper.RemoveIcons()
		MAHelper.ListAddPosition(CS , 1)
	end
end	

function Set2.OnUnitKilled(eventCode,   result,  isError,  abilityName,  abilityGraphic,   abilityActionSlotType,  sourceName,   sourceType,   targetName,   targetType,  hitValue,   powerType,   damageType,  log,  sourceUnitId,  targetUnitId,  abilityId,  overflow)
	NumK = NumK + 1
	if NumK >= Set2.rounds[CS].Waves[CurrWeave].nMOB then
		token = false 
		zo_callLater(function() token = true end, 4000)
		if CS == Set3.nR then
			d("Finito Stage")
			MAHelper.RemoveIcons()	
			do return end   
		end
		MAHelper.RemoveIcons()
		CurrWeave = CurrWeave +1
		if CurrWeave <= Set2.rounds[CS].nWave then
			NumK = 0			
			MAHelper.ListAddPosition(CS, CurrWeave)		
		else
			CurrWeave = 1
			NumK=0
			CS = CS +1
			if CS <= Set2.nR then 			
				MAHelper.ListAddPosition(CS, 1)	
			end
		end
	end
end
